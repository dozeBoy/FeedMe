package POJOs;


public class RecyclerItem {

    private String text;
    private int thumbnail;

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getThumbnail() {
        return thumbnail;
    }

    public void setThumbnail(int thumbnail) {
        this.thumbnail = thumbnail;
    }

}
