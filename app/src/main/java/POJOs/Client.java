package POJOs;


public class Client {
    private String denumireAgent;
    private String codFiscal;
    private String adresa;
    private String localitate;

    public String getDenumireAgent() {
        return denumireAgent;
    }

    public void setDenumireAgent(String denumireAgent) {
        this.denumireAgent = denumireAgent;
    }

    public String getCodFiscal() {
        return codFiscal;
    }

    public void setCodFiscal(String codFiscal) {
        this.codFiscal = codFiscal;
    }

    public String getAdresa() {
        return adresa;
    }

    public void setAdresa(String adresa) {
        this.adresa = adresa;
    }

    public String getLocalitate() {
        return localitate;
    }

    public void setLocalitate(String localitate) {
        this.localitate = localitate;
    }


}
